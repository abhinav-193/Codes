// Number and global method in es6

// the isFinite() Method
// the global isFinite() method returns false if the argument is Infinity of NaN

// The isNaN Method
// The global isNaN() method returns true if the argument is NaN. Otherwise returns false

// The Number.isInteger() Method
// The Number.isInteger() method returns true if the argument is an integer

console.log(isFinite("dafas"));
console.log(isFinite(2));

console.log(Number.isNaN("sadfhbdjk"));
console.log(isNaN(2));

console.log(Number.isInteger(3.4));
console.log(Number.isInteger(3));